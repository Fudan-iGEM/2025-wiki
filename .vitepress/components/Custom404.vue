<script setup>
import { ref, onMounted, onUnmounted } from 'vue'
import { gsap } from 'gsap'
import "../theme/tw.css"

const svgContainer = ref(null)
const svg404 = ref(null)
let animationTimeline = null

onMounted(() => {
  initAnimations()
})

const initAnimations = () => {
  if (!svg404.value) return
  
  animationTimeline = gsap.timeline({ repeat: -1 })
  
  const feet = svg404.value.querySelectorAll('.foot')
  feet.forEach((foot, index) => {
    animationTimeline.to(foot, {
      duration: 0.8,
      y: -15,
      ease: "bounce.out",
      repeat: -1,
      yoyo: true,
      delay: index * 0.2,
      transformOrigin: "center bottom"
    }, 0)
  })
  
  const tailDot = svg404.value.querySelector('.tail-dot')
  if (tailDot) {
    animationTimeline.to(tailDot, {
      duration: 2,
      rotation: 360,
      ease: "none",
      repeat: -1,
      transformOrigin: "center center"
    }, 0)
    
    animationTimeline.to(tailDot, {
      duration: 1.5,
      scale: 1.3,
      ease: "power2.inOut",
      repeat: -1,
      yoyo: true,
      transformOrigin: "center center"
    }, 0)
  }
  
  // 3. 黄色装饰点摆动
  const yellowDot = svg404.value.querySelector('.yellow-dot')
  if (yellowDot) {
    animationTimeline.to(yellowDot, {
      duration: 1.2,
      rotation: 15,
      ease: "power2.inOut",
      repeat: -1,
      yoyo: true,
      transformOrigin: "center center"
    }, 0)
    
    animationTimeline.to(yellowDot, {
      duration: 2,
      scale: 1.1,
      ease: "sine.inOut",
      repeat: -1,
      yoyo: true
    }, 0)
  }
  
  // 4. 眼睛眨眼效果
  const eyes = svg404.value.querySelectorAll('.eye')
  eyes.forEach((eye, index) => {
    animationTimeline.to(eye, {
      duration: 0.1,
      scaleY: 0.1,
      ease: "power2.inOut",
      repeat: -1,
      repeatDelay: 3,
      delay: index * 0.1,
      transformOrigin: "center center"
    }, 0)
  })
  
  // 5. 眼睛瞳孔跟随效果
  const eyePupil = svg404.value.querySelector('.eye-pupil')
  if (eyePupil) {
    animationTimeline.to(eyePupil, {
      duration: 2,
      x: 5,
      ease: "power2.inOut",
      repeat: -1,
      yoyo: true
    }, 0)
  }
  
  // 7. 三角形装饰旋转
  const triangle = svg404.value.querySelector('.triangle')
  if (triangle) {
    animationTimeline.to(triangle, {
      duration: 3,
      rotation: 360,
      ease: "none",
      repeat: -1,
      transformOrigin: "center center"
    }, 0)
  }
  

  // 9. 眼睛中心高光闪烁
  const eyeCenter = svg404.value.querySelector('.eye-center')
  if (eyeCenter) {
    animationTimeline.to(eyeCenter, {
      duration: 0.5,
      opacity: 0.3,
      ease: "power2.inOut",
      repeat: -1,
      yoyo: true,
      repeatDelay: 2
    }, 0)
  }
}

onUnmounted(() => {
  if (animationTimeline) {
    animationTimeline.kill()
  }
})
</script>

